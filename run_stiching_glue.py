    # we already did the padding.
    if data_args.pad_to_max_length:
        data_collator = default_data_collator
    elif training_args.fp16:
        data_collator = DataCollatorWithPadding(tokenizer, pad_to_multiple_of=8)
    else:
        data_collator = None
    eval_dataset = eval_dataset.remove_columns("idx")
    eval_dataloader = DataLoader(eval_dataset, collate_fn=data_collator, batch_size=768)

    model.eval()
    with torch.no_grad():
        for step, batch in enumerate(tqdm(eval_dataloader)):
            for k, v in batch.items():
                if isinstance(v, torch.Tensor):
                    batch[k] = v.to(training_args.device)
            outputs = model(**batch)
            logits = outputs[1]

            predictions = logits.argmax(dim=-1) if not is_regression else logits.squeeze()
            metric.add_batch(
                predictions=predictions,
                references=batch["labels"],
            )
        eval_metric = metric.compute()
        acc.append(eval_metric['accuracy'])
        logger.info(f"seed_{seed_x}_{seed_y} evaluation metric: {eval_metric}")
        logger.info(f"mean, std: {np.mean(acc), np.std(acc)}")


